package com.zyf.bilibili;

public class Bean_grid {

    String imgUrl ;
    String name;

    public Bean_grid(String imgUrl, String name) {
        this.imgUrl = imgUrl;
        this.name = name;
    }

    public String getImgUrl() {
        return imgUrl;
    }

    public String getName() {
        return name;
    }
}
