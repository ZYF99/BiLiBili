package com.zyf.bilibili.bean;

public class Bean_mainList {

    String name;
    String imgUrl;

    public Bean_mainList(String name, String imgUrl) {
        this.name = name;
        this.imgUrl = imgUrl;
    }

    public String getName() {
        return name;
    }
    public String getImgUrl() {
        return imgUrl;
    }

}
